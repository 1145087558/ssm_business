package com.qst.entity;

public class Calligraphy {

	private int id;
	private String title;
	private String content;
	private String author;
	private String image;
	private int art_status;
	private int type_status;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getContent() {
		return content;
	}
	public void setContent(String content) {
		this.content = content;
	}
	public String getAuthor() {
		return author;
	}
	public void setAuthor(String author) {
		this.author = author;
	}
	public String getImage() {
		return image;
	}
	public void setImage(String image) {
		this.image = image;
	}
	public int getArt_status() {
		return art_status;
	}
	public void setArt_status(int art_status) {
		this.art_status = art_status;
	}
	public int getType_status() {
		return type_status;
	}
	public void setType_status(int type_status) {
		this.type_status = type_status;
	}
	
}
